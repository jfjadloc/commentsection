          <p class="ven">
  <b>Dental crowns and bridges</b> are custom-fitted tooth prosthetics that are used to replace or restore damaged or missing teeth. Crowns – also known as caps – are fixed over the surfaces of natural tooth structures or dental implants. Bridges are used to fill in the gaps left by missing teeth and are anchored in place by the natural teeth or crowns nearest the empty space. Both crowns and bridges are non-removable and must be cemented in place by a licensed dentist. Patients who get crown or bridges to restore their smiles achieve both the function and appearance of natural, healthy teeth.
  </p>
<h1 class="fact">
  <font color="#b666d2">Fact...</font>
</h1>
    <p class="ven1">
            That the <b>Etruscan civilization</b> were the first to use crowns as a means of restoring damaged teeth? In fact, the materials they used – ivory, gold, and bones – were still the standard in dentistry as recently as the 20th century, when porcelain crowns were first invented. Today, crowns and bridges are customized specifically for the patient’s bite and can usually be placed in as little as one or two dental visits. With proper cleaning and regular dental check-ups, crowns and bridges can last many years, or even a lifetime.
          </p>



<h1 class="fact2">
  <font color="#b666d2">Frequently Asked Questions...</font>
</h1>
  <p class="ven2">
       <b> Is a crown or bridge right for me?</b>
If you have a tooth that is damaged or decayed, but still intact, a dental crown may be right for you. If your tooth is missing, but its former position is surrounded by other tooth structures, a bridge may be the solution for you. Schedule an office consultation to determine whether you could benefit from crowns or bridges.
          </p>


<h1 class="fact3">
  <font color="#b666d2">Post-treatment Care instructions...</font>
</h1>
  <p class="ven3">
     Your teeth will need time to heal following the <b>crown and bridge placement process</b>, so it is normal for you to experience some sensitivity – especially to hot and cold. Additionally, you may experience soreness in the gums surrounding your restorations, though this is usually manageable with ibuprofen and should subside within a few days.
          </p>






