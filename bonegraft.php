          <p class="ven">
   <b>Bone grafting </b> – also known as ridge and sinus augmentation – is a procedure used to build up the bone beneath the surface of the gums. The bones in the upper and lower arch of the mouth are responsible for supporting the teeth. When one or more teeth fall out, bone resorption occurs naturally. When patients decide to replace missing teeth with dental implants, insufficient bone mass can prevent them from qualifying for the procedure. Bone grafting addresses inadequate bone mass by filling in eroded areas with bone harvested from the patient’s own body, or that of a cadaver or animal. The new bone helps promote new bone growth that will eventually support a dental implant.
  </p>
