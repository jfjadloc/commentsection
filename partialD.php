          <p class="ven">
   Like a bridge, <b>partial dentures </b> rest on surrounding teeth to fill in the gaps where one or more teeth are missing. But unlike a bridge, partial dentures are fully removable by the wearer. Partials are affordable alternatives to other types of dental prosthetics and are custom-made to blend in with each patient’s natural teeth. It takes a little time to adapt to new partials, but many people find that they reclaim much of their original function and aesthetics with partial dentures in place.
  </p>
<h1 class="fact">
  <font color="#b666d2">Fact...</font>
</h1>
    <p class="ven1">
            <b>Partial dentures</b>require gentle care and frequent cleaning. Once you get your new partial dentures, you’ll need to:

Keep them moist at all times by soaking them in a denture solution when not in use
Gently brush your dentures daily using a soft-bristled tooth brush
Be careful not to drop your partial dentures, as they may break
Avoid exposing your partial to hot temperatures that could cause warping
          </p>



<h1 class="fact2">
  <font color="#b666d2">Frequently Asked Questions...</font>
</h1>
  <p class="ven2">
       <b>Am I a candidate for partial dentures?</b>
You may be a candidate for partial dentures if you have one or more missing teeth, and the space they once filled is surrounded by other teeth or permanent restorations. To find out more about whether partial dentures are right for you, schedule a consultation with your dentist.
          </p>


<h1 class="fact3">
  <font color="#b666d2">Post-treatment Care instructions...</font>
</h1>
  <p class="ven3">
    <b> Yes. Good oral health is still important</b> – even if you have a few missing teeth. In addition to caring for your new dental prosthetic, you’ll also need to brush your gums and tongue twice daily to stimulate circulation and remove bacteria that could cause gum disease. Continue seeing your dentist twice yearly for exams and cleanings, and be sure to bring your partial dentures along to each visit. After a few years, you may find that your partial needs to be rebased to better fit the changes to the bone structure in your mouth.
          </p>






