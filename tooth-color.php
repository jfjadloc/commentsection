          <p class="ven">
  <b>Composite fillings</b> – also known as tooth-colored fillings – are dental restorations designed to be inconspicuous and natural in appearance. They blend well with the teeth and appear more natural than amalgam fillings, which are darker and more easily seen by other people. Composite fillings are made of ceramic and plastic compounds that chemically bond to the teeth. They can be used to fill in decayed areas of the teeth, as well as to help repair chipped or broken teeth. Most dentists use composite restorations to treat the teeth closest to the front of the mouth, as they are more noticeable when patients smile. However, advancements in dental technology and the composition of composite fillings have made it possible for Auburn dentists to also use tooth-colored fillings on molars, which receive more wear than other teeth.
  </p>
<h1 class="fact">
  <font color="#b666d2">Fact...</font>
</h1>
    <p class="ven1">
         <b>  that composite fillings allow dentists to preserve more of the natural tooth structure?</b> This is because composite materials chemically bond to the surface of the tooth like an adhesive. The process takes slightly longer to complete than traditional amalgam fillings, but patients can preserve more of the natural portion of the teeth while enjoying a restoration that is discreet and understated.
          </p>



