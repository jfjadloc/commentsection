          <p class="ven">
   <b>Dental veneers</b> – also known as laminates – are used to cosmetically enhance the appearance of one or more teeth. Veneers are very thin, porcelain or resin shells that are customized for a desirable color and shape. They are bonded to the surface of the teeth to reshape broken, misshapen or irregular teeth, as well as provide a solution for discolored teeth that do not respond to traditional whitening treatments. Patients with veneers typically achieve a natural tooth appearance that is well-tolerated by the gums and also resistant to future stains.
  </p>
